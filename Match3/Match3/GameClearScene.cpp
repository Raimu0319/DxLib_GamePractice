#include "GameClearScene.h"
#include "DxLib.h"
#include "SceneManager.h"

//変数宣言
int GameClearImage;
int GameClearSE;
int GameClearFlag;

/*
	ゲームクリア画面：初期化処理
	引数：なし
	戻り値；エラー情報
*/

int GameClearScene_Initialize(void) 
{
	int ret = 0;

		//画像の読み込み
	GameClearImage = LoadGraph("images/gameclear.png");
		//音源の読み込み
	GameClearSE = LoadSoundMem("sounds/gameclear_se.mp3");

	GameClearFlag = 0;

	//エラーチェック
	if (GameClearImage == -1)
	{
		ret = -1;
	}
	if (GameClearSE == -1)
	{
		ret = -1;
	}

	return ret;

}
